import java.util.Date;

public class Disponibilidade {
	private Date data;
	private boolean ocupacao;
	
	public Date getData() {
		return data;
	}
	public void setData(Date data) {
		this.data = data;
	}
	public boolean isOcupacao() {
		return ocupacao;
	}
	public void setOcupacao(boolean ocupacao) {
		this.ocupacao = ocupacao;
	}

}
